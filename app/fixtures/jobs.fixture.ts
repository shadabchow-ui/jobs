function buildRichJob(fixture: JobFixture): Job {
  const entry = companyLocationMap[fixture.slug] ?? { locId: "loc-005", catIds: ["cat-001"] };
  const jobCompany = JOB_COMPANY_LOOKUP[fixture.companySlug];
  if (!jobCompany) throw new Error(`Company ${fixture.companySlug} not found`);

  const salaryRange = fixture.salaryRange;
  const salary: SalaryInsight | null = salaryRange
    ? {
        min: null,
        max: null,
        currency: "USD",
        period: "yearly",
        displayRange: salaryRange,
      }
    : null;

  return {
    id: fixture.id,
    slug: fixture.slug,
    title: fixture.title,
    company: jobCompany,
    location: findLocation(entry.locId),
    categoryIds: entry.catIds,
    categories: findCategories(entry.catIds),
    salary,
    description: fixture.description,
    requirements: fixture.requirements,
    responsibilities: fixture.responsibilities,
    benefits: fixture.benefits,
    postedAt: fixture.postedAt,
    closingAt: null,
    source: fixture.source,
    sourceId: `fx-${fixture.id}`,
    applyUrl: fixture.applyUrl,
    aiSummary: fixture.aiSummary,
    detectedSkills: fixture.detectedSkills,
    seniority: fixture.seniority,
    remoteType: fixture.remoteType,
    salaryNote: fixture.salaryNote,
    matchScore: fixture.matchScore,
    matchedSkills: fixture.matchedSkills,
    missingSkills: fixture.missingSkills,
    jobQualityScore: fixture.jobQualityScore,
  };
}

export function getRichJobs(): Job[] {
  return JOBS_FIXTURES.map(buildRichJob);
}

