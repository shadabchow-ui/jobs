export function getJobBySlug(slug: string): JobFixture | undefined {
  return JOBS_FIXTURES.find((j) => j.slug === slug);
}

export function getRichJobBySlug(slug: string): Job | undefined {
  return getRichJobs().find((j) => j.slug === slug);
}

export function validateJobFixture(job: JobFixture): string[] {
  const errors: string[] = [];
  if (!job.id) errors.push("Job missing id");
  if (!job.slug) errors.push("Job missing slug");
  if (!job.title) errors.push("Job missing title");
  if (!job.company) errors.push("Job missing company");
  if (!job.description) errors.push("Job missing description");
  if (!Array.isArray(job.tags)) errors.push("Job tags must be an array");
  if (!Array.isArray(job.responsibilities)) errors.push("Job responsibilities must be an array");
  if (!Array.isArray(job.requirements)) errors.push("Job requirements must be an array");
  if (!Array.isArray(job.benefits)) errors.push("Job benefits must be an array");
  return errors;
}

